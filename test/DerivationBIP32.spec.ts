  describe('BIP32 ExtendedKey should', () => {
    extendedKeys.map((extendedKey) => {
      // create master key node
      const masterKey = ExtendedKey.createFromSeed(extendedKey.seedHex, Network.BITCOIN);

      it(extendedKey.label + ': create correct master extended private key', () => {
        expect(masterKey.toBase58()).to.be.equal(extendedKey.masterPrv);
      });

      it(extendedKey.label + ': create correct master extended public key', () => {
        const masterPub = masterKey.getPublicNode();
        expect(masterPub.toBase58()).to.be.equal(extendedKey.masterPub);
      });

      it(
        extendedKey.label +
          ': derive correct extended public key given seed (' +
          extendedKey.seedHex.length / 2 +
          ' bytes) and paths',
        () => {
          // iterate through paths to derive
          extendedKey.neutered.map((neuteredKey) => {
            const childDerived = masterKey.derivePath(neuteredKey.path);
            expect(childDerived.getPublicNode().toBase58()).to.be.equal(neuteredKey.key);
          });
        },
      );

      it(
        extendedKey.label +
          ': derive correct extended private key given seed (' +
          extendedKey.seedHex.length / 2 +
          ' bytes)  and paths',
        () => {
          // iterate through paths to derive
          extendedKey.nonNeutered.map((nonNeuteredKey) => {
            const childDerived = masterKey.derivePath(nonNeuteredKey.path);
            expect(childDerived.toBase58()).to.be.equal(nonNeuteredKey.key);
          });
        },
      );
    });
  });
